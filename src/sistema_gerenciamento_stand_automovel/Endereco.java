
package sistema_gerenciamento_stand_automovel;

public class Endereco {
    
    
    private String cidade;
    private String rua;
    private String numero_edificio;

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public String getRua() {
        return rua;
    }

    public void setRua(String rua) {
        this.rua = rua;
    }

    public String getNumero_edificio() {
        return numero_edificio;
    }

    public void setNumero_edificio(String numero_edificio) {
        this.numero_edificio = numero_edificio;
    }
}
